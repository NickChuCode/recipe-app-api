"""
Calculator functions
"""

def add(x, y):
    """Return the sum of the two argument

    Args:
        x (Number): number1
        y (Number): number2

    Returns:
        Number: sum of number1 and number2
    """
    return x + y

def subtract(x, y):
    """Return the result of x subtract y

    Args:
        x (Number): 减数
        y (Number): 被减数

    Returns:
        Number: x - y 的结果
    """
    return x - y